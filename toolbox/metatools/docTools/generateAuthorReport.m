function generateAuthorReport()
%% Generate the html contributing author report
% PMTKneedsMatlab 
dest       = fullfile(pmtk3Root(), 'docs', 'authors');
outputFile = fullfile(dest, 'fileAuthors.html');
colNames   = {'FILE NAME', 'AUTHOR(S)', 'SOURCE URL', 'DATE'};
pmtkRed    = getConfigValue('PMTKred');
searchDirs = tokenize(getConfigValue('PMTKcodeDirs'), ','); 
files      = []; 
for i=1:numel(searchDirs)
   files = [files; findAllFilesWithTag(...
       'PMTKauthor', true, fullfile(pmtk3Root(), searchDirs{i}));];%#ok
end
fnames     = filenames(files); 
nfiles     = numel(files); 
data       = repmat({'&nbsp;'}, nfiles, numel(colNames)); 
for f = 1:nfiles
   [tags, lines] = tagfinder(files{f}); 
   map = createStruct(tags, lines); 
   data{f, 1} = googleCodeLink(files{f}, fnames{f}); 
   data{f, 2} = map.PMTKauthor;
   if isfield(map, 'PMTKurl')
       data{f, 3} = sprintf(' <a href="%s"> website ',map.PMTKurl);
   end
   if isfield(map, 'PMTKdate')
      data{f, 4} = map.PMTKdate; 
   end
end
%% Sort by filename
perm = sortidx(lower(fnames)); 
data = data(perm, :); 
%%
header = [...
    sprintf('<font align="left" style="color:%s"><h2>Contributed Files</h2></font>\n', pmtkRed),...
    sprintf('<br>Revision Date: %s<br>\n', date()),...
    sprintf('<br>Auto-generated by %s.m<br>\n', mfilename()),...
    sprintf('<br>\n')...
    ];
htmlTable('data'           , data                                  , ...
    'colNames'             , colNames                              , ...
    'doSave'               , true                                  , ...
    'filename'             , outputFile                            , ...
    'colNameColors'        , {pmtkRed, pmtkRed, pmtkRed, pmtkRed}  , ...
    'header'               , header                                , ...
    'dataAlign'            , 'left'                                , ...
    'caption'              , '<br> <br>'                           , ...
    'captionLoc'           , 'bottom'                              , ...
    'doshow'               , false);
end